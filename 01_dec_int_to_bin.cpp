/*
Программа: Перевод целого числа из десятичной системы в двоичную
Раздел методички: Раздел 1. Перевод целых чисел из 10СС в 2СС
Описание: Реализует перевод положительного целого числа из 10-й СС в 2-ю СС с подробным выводом шагов
*/


#include <iostream> // Библиотека для ввода-вывода


using namespace std; // Пространство имен std


int main() {
    // Заголовок и ввод данных
    cout << "=== Программа перевода числа из 10 СС в 2 СС ===\n";
    cout << "Введите число от 0 до 255 в 10 СС для перевода: ";
    int number_int; // Исходное число в 10 СС
    cin >> number_int; // Ввод исходного числа в 10 СС
    cout << "Перевод числа " << number_int << " из 10 СС в 2 СС:\n";

    // Проверка на нулевое число (0 в 10 СС = 0 в 2 СС)
    if (number_int == 0) {
        cout << "Результат перевода: 0\n";
        return 0;
    }

    // Основной рабочий блок
    string number_bin; // Итоговое число в 2 СС
    int step = 1; // Счетчик шагов
    while (number_int > 0) {
        int ost = number_int % 2; // Вычисление остатка от деления на 2
        number_bin = to_string(ost) + number_bin; // Добавление остатка в начало строки
        cout << "Шаг " << step << ": число = " << number_int
                << ", остаток = " << ost << '\n';
        number_int = number_int / 2; // Целочисленное деление на 2
        step = step + 1; // Увеличение счетчика шагов
    }

    cout << "Результат перевода: " << number_bin << '\n';
    return 0;
}
